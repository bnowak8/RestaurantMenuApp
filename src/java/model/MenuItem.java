package model;

/**
 *
 * @author mashit
 */
public class MenuItem {
    private int id;
    private String name;
    private double price;
    
    public void MenuItem(int id, String name, double price){
        this.id = id;
        this.name = name;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

    @Override
    public String toString() {
        return "MenuItem{" + "id=" + id + ", itemName=" + name 
                + ", itemPrice=" + price + '}';
    }
    
}
